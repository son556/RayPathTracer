#include "libft.h"
#include <stdlib.h>

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	size_t	i;
	int		result;

	i = 0;
	result = 0;
	while (i < n && (s1[i] || s2[i]))
	{
		if ((unsigned char)s1[i] != (unsigned char)s2[i])
		{
			result = (int)(unsigned char)s1[i] - (int)(unsigned char)s2[i];
			return (result);
		}
		++i;
	}
	return (result);
}
